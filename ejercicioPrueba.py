#importamos la libreria para usar Tkinter
import tkinter as tk
#Crea la ventana
ventana = tk.Tk()

#Establecemos el tamaño de la ventana
ventana.geometry('600x400')

#Establecemos el título de la ventana
ventana.title('Nueva Ventana')

#Este comando siempre debe ir al final del codigo
# por que este comando es para ejecutar la ventana
ventana.mainloop()